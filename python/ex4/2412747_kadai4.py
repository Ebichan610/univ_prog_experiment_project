import cv2
import tkinter as tk
import numpy as np

def processing_threshold():
    img = cv2.imread("./Apple.bmp")
    red_upper = np.uint8(red_max.get())
    red_lower = np.uint8(red_min.get())
    blue_upper = np.uint8(blue_max.get())
    blue_lower = np.uint8(blue_min.get())
    green_upper = np.uint8(green_max.get())
    green_lower = np.uint8(green_min.get())
    bgrUpper = np.array([blue_upper, green_upper, red_upper])
    bgrLower = np.array([blue_lower, green_lower, red_lower])
    img_threshold = cv2.inRange(img, bgrLower, bgrUpper)
    cv2.imshow("Apple", img)
    cv2.imshow("Apple_result", img_threshold)
    cv2.waitKey()

root = tk.Tk()
root.title("2412747 Pythonで赤色抽出")
root.geometry("300x200")
tk.Label(root, text="赤").grid(column=1, row=0, sticky=tk.W)
tk.Label(root, text="青").grid(column=2, row=0, sticky=tk.W, padx=10)
tk.Label(root, text="緑").grid(column=3, row=0, sticky=tk.W, padx=10)
tk.Label(root, text="上限").grid(column=0, row=1)
tk.Label(root, text="下限").grid(column=0, row=2)
red_max = tk.Entry(root)
red_max.configure(width=5)
red_max.grid(column=1, row=1)
red_min = tk.Entry(root)
red_min.configure(width=5)
red_min.grid(column=1, row=2)
blue_max = tk.Entry(root)
blue_max.configure(width=5)
blue_max.grid(column=2, row=1, padx=10)
blue_min = tk.Entry(root)
blue_min.configure(width=5)
blue_min.grid(column=2, row=2, padx=10)
green_max = tk.Entry(root)
green_max.configure(width=5)
green_max.grid(column=3, row=1, padx=10)
green_min = tk.Entry(root)
green_min.configure(width=5)
green_min.grid(column=3, row=2, padx=10)
tk.Button(root, text="閾値処理", command=processing_threshold).place(x=140,y=80)

root.mainloop()
